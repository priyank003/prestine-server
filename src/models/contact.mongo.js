const mongoose = require("mongoose");

const contactUsSchema = new mongoose.Schema(
  {
    fullName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },

    phoneNo: {
      type: Number,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const Contact = mongoose.model("ContactQuery", contactUsSchema);

module.exports = Contact;
